            <h5>Bahan-bahan:</h5>
            <ul>
                <li> 200 gram tepung ketan putih</li>
                <li> 50 gram tepung tapioka (bisa diganti kentang kukus dihaluskan)</li>
                <li> 5 sdm caster sugar (bisa diganti gula pasir biasa)</li>
                <li> 1/4 sdt garam</li>
                <li> 190 ml air hangat</li>
                <li>100 gram wijen</li>
                <li>secukupnya minyak goreng</li>
            </ul>
            <hr>
            <h5>Bahan isian :</h5>
            <ul>
                <li> 100 gram kacang hijau kupas, rendam 1-2 jam (sy rendam 1/2 hari)</li>
                <li> 150 ml santan kental (kara)</li>
                <li> 1/4 sdt garam</li>
                <li> 5 sdm gula pasir</li>
                <li> 1 sdm vanilla extract (bisa pakai 1/2 sdt vanila bubuk)</li>
            </ul>
            <hr>
            <h5>Langkah :</h5>
            <ul>
                <li>1. Buat isian: Rebus kacang hijau kupas, sampai empuk, tiriskan, blender dengan 50 ml santan sampai halus</li>
                <li>2. Siapkan wajan/teflon masukkan kacang hijau blender tadi beri santan gula garam dan vanilla extract. Aduk2 sampai kalis. Note: karena saya pakai santan banyak, diawal2 agak encer ya, tapi masak terus saja, nanti pasti jadi kental.</li>
                <li>3. Bulat2kan sekitar 10-12 gram.Sisihkan</li>
                <li>4. Buat kulitnya: Campur semua tepung2an gula juga garam, aduk rata tuang air sedikit demi sedikit hentikan jika sudah bs dibentuk</li>
                <li>5. Bagi menjadi menjadi beberapa bagian beri warna (saya merah orange, hijau, biru, ungu dan putih). Timbang masing2 20 gram atau sesuai selera ya</li>
                <li>Pipihkan lalu beri isian dan bulatkan. Cara saya agar bulat banget, saya bulatkan dengn bantuan sendok.</li>
                <li>7. Celup ke dalam air, guling2kan ke wijen.</li>
                <li>8. Goreng sampai matang, angkat, tiriskan (tips2 menggoreng onde2 dapat di baca di blog yaa)</li>
                <li>9. Sajikan</li>
            </ul>           
